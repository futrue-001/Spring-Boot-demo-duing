package com.xj.bean;

public class Food {
    //肉类
    private String meat;
    //米类
    private String rice;



    public String getMeat() {
        return meat;
    }

    public void setMeat(String meat) {
        this.meat = meat;
    }

    public String getRice() {
        return rice;
    }

    public void setRice(String rice) {
        this.rice = rice;
    }
}
