package com.xj.bean;


public class Soup {
    // Soup 汤类
    //西红柿鸡蛋汤
    private String tomato;
    private String egg;

    public String getTomato() {
        return tomato;
    }

    public void setTomato(String tomato) {
        this.tomato = tomato;
    }

    public String getEgg() {
        return egg;
    }

    public void setEgg(String egg) {
        this.egg = egg;
    }
}
